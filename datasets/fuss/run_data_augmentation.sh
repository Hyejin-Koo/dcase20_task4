# TOP LEVEL VARIABLES TO BE CHANGED

# Download directory to download data into.
DOWNLOAD_DIR=/data/download

# Random seed to use for augmented data generation.
RANDOM_SEED=2020

# Number of train and validation examples to generate.
NUM_TRAIN=20000
NUM_VAL=1000

# This is the main directory where the raw data that will be used in data
# augmentation will be downloaded.
RAW_DIR=/data/dcase2020_task4_ss_raw

# This is the main directory where the augmented data will reside.
# _${RANDOM_SEED} will be appended to this path, so multiple versions
# of augmented data can be generated by only changing the random seed.
AUG_ROOT_DIR=/data/dcase2020_task4_ss_augment

SCRIPT_PATH=`dirname $0`

bash ${SCRIPT_PATH}/get_raw_data.sh ${DOWNLOAD_DIR} ${RAW_DIR}

bash ${SCRIPT_PATH}/install_dependencies.sh

bash ${SCRIPT_PATH}/run_scaper.sh ${RAW_DIR} ${AUG_ROOT_DIR} ${RANDOM_SEED} ${NUM_TRAIN} ${NUM_VAL}

bash ${SCRIPT_PATH}/run_reverb_and_mix.sh ${RAW_DIR} ${AUG_ROOT_DIR} ${RANDOM_SEED}

echo Done!
echo Check directory ${AUG_ROOT_DIR}_${RANDOM_SEED} for to access the augmented
echo train/validation data.


